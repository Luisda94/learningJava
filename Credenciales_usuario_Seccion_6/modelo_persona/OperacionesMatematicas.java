package modelo_persona;

public class OperacionesMatematicas {
    // Método para calcular la raíz cuadrada
	// Aplicando Math
    public double calcularRaizCuadrada(double numero) {
        return Math.sqrt(numero);
    }

    // Método para obtener el valor de Pi
    public double obtenerValorPi() {
        return Math.PI;
    }

    // Método para calcular la potencia de un número
    public double calcularPotencia(double base, double exponente) {
        return Math.pow(base, exponente);
    }
}
